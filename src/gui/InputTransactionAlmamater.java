    public TableModel buildAlmamaterTableModel(Profil profil) throws SQLException, KasirException{
       String columnNames[] = {"Kelas","Detail / History", "Transaksi Almamater", "Biaya", "Sisa", "Pembayaran", "IuranID"};
       Set<Almamater> almamaterFilters = new HashSet<>();
       almamaterFilters.add(new Almamater(profil.noInduk,null, null, 0F, null));
       Map<Long, Almamater> searchResultMap = new HashMap<>();
       searchResultMap = Control.exactFilterSelectIurans(Iuran.Tipe.Almamater, almamaterFilters);
       
       Set<Long> transactDetailIDsTemp = new HashSet<Long>();
       int j = 0;
       almamaterS = new ArrayList<>();
       ArrayList<Long> almamaterSPaid = new ArrayList<Long>();
       ArrayList<String> almamaterSTransactionDate = new ArrayList<String>();
       ArrayList<AlmamaterTransactionDetail> stds = new ArrayList<AlmamaterTransactionDetail>();
       if(searchResultMap.size() > 0){
        for(Map.Entry<Long, Almamater> entry: searchResultMap.entrySet()){
            transactDetailIDsTemp.clear();
            transactDetailIDsTemp = entry.getValue().transactDetailIDs;
            long paidAlmamater = 0l;
            String lastTransactionDate = "";
            SimpleDateFormat formatIndo = new SimpleDateFormat("dd-MM-yyyy");
            AlmamaterTransactionDetail std = new AlmamaterTransactionDetail();
            stds.clear();
            for(Long l : transactDetailIDsTemp){
                std = Control.selectTDetail(TransactionDetail.Tipe.AlmamaterTransaction, l);
                paidAlmamater += std.amount;
                lastTransactionDate = formatIndo.format(std.lastUpdateDate.toDate());
                almamaterSTransactionDate.add(lastTransactionDate);
                stds.add(std);
            }
            
            if(entry.getValue().amount > paidAlmamater){
                almamaterS.add(entry.getValue());
                almamaterSPaid.add(paidAlmamater);
            }
            j++;
        }
       }
       Object[][] data = new Object[almamaterS.size()][columnNames.length];
       for(int i =0; i < almamaterS.size(); i++){
            
            data[i][0]= almamaterS.get(i).chargedLevel;
            data[i][1]= "Detail";
            data[i][2]= almamaterS.get(i).transactName;
            data[i][3]= almamaterS.get(i).amount;
            data[i][4]= almamaterS.get(i).amount-almamaterSPaid.get(i);
            data[i][5]= 0f;
            data[i][6]= almamaterS.get(i).id;
//            if(almamaterS.get(i).transactDetailIDs.size()>0){
//                data[i][3] = new Boolean(true);
//            }else{
//                data[i][3] = new Boolean(false);
//            }
       }
       itfs.unpaidAlmamater = calculateUnpaidAlmamater(profil);
       if(data.length > 0){
       TableModel tm = new DefaultTableModel(data, columnNames){
           @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false for editable
               if(itfs.jDialogTransactionSummary.isVisible()){
                    return false;
               }else{
                    return true;
               }
            }
            public Class getColumnClass(int c) {
                return getValueAt(0, c).getClass();
            }
       };
       
       return tm;
       }else{
           TableModel tm = new DefaultTableModel(columnNames, columnNames.length);
           return tm;
       }
   }
    
    public TableModel buildAlmamaterSubmitTableModel(Profil profil, List<Almamater> stdb) throws SQLException, KasirException{
       String columnNames[] = {"Kelas","Detail / History", "Transaksi Almamater", "Biaya", "Sisa", "Pembayaran", "IuranID"};
       
       Set<Almamater> almamaterFilters = new HashSet<>();
       Set<Long> almamaterIDs = new HashSet<>();
       for(int i = 0 ; i < stdb.size(); i++){
           almamaterIDs.add(stdb.get(i).id);
       }
       almamaterFilters.addAll(stdb);
       Map<Long, Almamater> searchResultMap = new HashMap<>();
       //searchResultMap = Control.exactFilterSelectIurans(Iuran.Tipe.Almamater, almamaterFilters);
       searchResultMap = Control.selectIurans(Iuran.Tipe.Almamater, almamaterIDs);
       Set<Long> transactDetailIDsTemp = new HashSet<Long>();
       int j = 0;
       long paidAlmamater = 0l;
       almamaterS = new ArrayList<>();  
       ArrayList<Long> almamaterSPaid = new ArrayList<Long>();
       ArrayList<Kalender> almamaterSTransactionDate = new ArrayList<Kalender>();
       if(searchResultMap.size() > 0){
        for(Map.Entry<Long, Almamater> entry: searchResultMap.entrySet()){
            transactDetailIDsTemp.clear();
            transactDetailIDsTemp = entry.getValue().transactDetailIDs;
            paidAlmamater = 0l;
            Kalender lastTransactionDate = new Kalender();
            AlmamaterTransactionDetail std = new AlmamaterTransactionDetail();
            for(Long l : transactDetailIDsTemp){
                std = Control.selectTDetail(TransactionDetail.Tipe.AlmamaterTransaction, l);
                paidAlmamater += std.amount;
                lastTransactionDate = std.lastUpdateDate;
                almamaterSTransactionDate.add(lastTransactionDate);
            }
            if(transactDetailIDsTemp.isEmpty()){
                almamaterSTransactionDate.add(new Kalender());
            }
            
            if(entry.getValue().amount > paidAlmamater){
                almamaterS.add(entry.getValue());
                almamaterSPaid.add(paidAlmamater);
                
            }
            j++;
        }
       }
       Object[][] data = new Object[almamaterS.size()][columnNames.length];
       for(int i =0; i < almamaterS.size(); i++){
            data[i][0]= almamaterS.get(i).chargedLevel;
            data[i][1]= almamaterSTransactionDate.get(i);
            data[i][2]= almamaterS.get(i).transactName;
            data[i][3] = almamaterS.get(i).amount;
            data[i][4] = almamaterS.get(i).amount - stdb.get(i).amount - almamaterSPaid.get(i);
            data[i][5] = stdb.get(i).amount;
            data[i][6] = almamaterS.get(i).id;
            itfs.unpaidAlmamater = itfs.unpaidAlmamater - stdb.get(i).amount;
       }
       
      
       if(data.length > 0){
       TableModel tm = new DefaultTableModel(data, columnNames){
           @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false for editable
               if(itfs.jDialogTransactionSummary.isVisible()){
                    return false;
               }else{
                    return true;
               }
            }
            public Class getColumnClass(int c) {
                return getValueAt(0, c).getClass();
            }
       };
       
       return tm;
       }else{
           TableModel tm = new DefaultTableModel(columnNames, columnNames.length);
           return tm;
       }
   }
    
    private float calculateUnpaidAlmamater(Profil profil) throws SQLException, KasirException {
       float retVal = 0;
       Set<Almamater> almamaterFilters = new HashSet<>();
       almamaterFilters.add(new Almamater(profil.noInduk,null, null, 0F, null));
       Map<Long, Almamater> searchResultMap = new HashMap<>();
       Set<Long> transactDetailIDsTemp = new HashSet<Long>();
       searchResultMap = Control.exactFilterSelectIurans(Iuran.Tipe.Almamater, almamaterFilters);
       almamaterS = new ArrayList<>();
       
       if(searchResultMap.size() > 0){
        for(Map.Entry<Long, Almamater> entry: searchResultMap.entrySet()){
            transactDetailIDsTemp.clear();
            transactDetailIDsTemp = entry.getValue().transactDetailIDs;
            long paidAlmamater = 0l;
            for(Long l : transactDetailIDsTemp){
                paidAlmamater += Control.selectTDetail(TransactionDetail.Tipe.AlmamaterTransaction, l).amount;
            }
            
            if(entry.getValue().amount > paidAlmamater){
                almamaterS.add(entry.getValue());
            }
            retVal += entry.getValue().amount - paidAlmamater;
        }
       }
       unpaidAlmamater = retVal;
       System.out.println("unpaid Almamater");
       System.out.println(unpaidAlmamater);
       return retVal;
    }
    
