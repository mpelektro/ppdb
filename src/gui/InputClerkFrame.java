    private void checkPassword() {
        char[] password1= jPasswordField1.getPassword();
        char[] password2= jPasswordField2.getPassword();
        pass1="";
        pass2="";
        for(int i = 0; i < password1.length; i++){
            pass1= pass1.concat(Character.toString(password1[i]));
        }
        for(int i = 0; i < password2.length; i++){
            pass2 = pass2.concat(Character.toString(password2[i]));
        }
        if(pass1.equals(pass2)){
            jButtonSave.setEnabled(validatePanel());
            jLabelPasswordNotMatched.setVisible(false);
        }else{
            jButtonSave.setEnabled(false);
            jLabelPasswordNotMatched.setVisible(true);
        }
    }
    private boolean doInsertClerk(){
       if(pass1.equals(pass2)){
//            Clerk cl = new Clerk(); // temporary, has to be deleted soon
            
            Clerk clerk = new Clerk(jNIPTextField.getText(), jNamaTextField.getText(), jJabatanTextField.getText(), jUserNameTextField.getText(), pass1);
            try {
//                cl = cl.login("rusly", "123456"); // temporary, has to be deleted soon
//                cl.insertClerk(clerk);
                Control.insertClerk(clerk);
                JOptionPane.showMessageDialog(rootPane, "Input Kasir Berhasil");
                return true;
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
                JOptionPane.showMessageDialog(rootPane, "Input Kasir Gagal!\r\n".concat(ex.toString()));
                return false;
            } catch (KasirException ex) {
                Exceptions.printStackTrace(ex);
                JOptionPane.showMessageDialog(rootPane, "Input Kasir Gagal!\r\n".concat(ex.toString()));
                return false;
            }
       }else{
           return false;
       }
    }
    
