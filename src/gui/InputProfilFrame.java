    private TableModel buildInitialTableModel(ArrayList<Profil> profils){
       String columnNames[] = {"Nomor Induk"
               , "Nama Siswa"
               , "Jenis Kelamin"
               , "Agama"
               , "Tanggal Lahir"
               , "Tempat Lahir"
               , "Alamat"
               , "Nama Ayah"
               , "Nama Ibu"
               , "Telepon 1"
               , "Telepon 2"
               , "Tanggal Masuk"
               , "Sekolah"
               , "Kelas"
               , "Sub Kelas"};
       Object[][] data = new Object[profils.size()][15];
       
       for(int i = 0 ; i< profils.size(); i++){
           data[i][0] = profils.get(i).noInduk;
           data[i][1] = profils.get(i).biodata.nama;
           data[i][2] = profils.get(i).biodata.kelamin;
           data[i][3] = profils.get(i).biodata.agama;
           data[i][4] = profils.get(i).biodata.tanggalLahir;
           data[i][5] = profils.get(i).biodata.tempatLahir;
           data[i][6] = profils.get(i).biodata.alamat;
           data[i][7] = profils.get(i).biodata.namaAyah;
           data[i][8] = profils.get(i).biodata.namaIbu;
           data[i][9] = profils.get(i).biodata.telpon1;
           data[i][10] = profils.get(i).biodata.telpon2;
           data[i][11]= profils.get(i).tanggalMasuk;
           data[i][12]= profils.get(i).currentLevel.level1;
           data[i][13]= profils.get(i).currentLevel.level2;
           data[i][14]= profils.get(i).currentLevel.level3;
       }    
       
       TableModel tm = new DefaultTableModel(data, columnNames){
           @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false for editable
                return true;
            }
       };
       
       return tm;
       
   }
    
