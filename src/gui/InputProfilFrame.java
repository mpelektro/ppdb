    private void checkNomorPendaftaran() {
        String prefixNomorInduk = "";
        try {
            prefixNomorInduk = jNomorIndukTextField.getText(0,1).toUpperCase();
        } catch (BadLocationException ex) {
            Exceptions.printStackTrace(ex);
        }
        switch(prefixNomorInduk){
            case "A":
                jComboBoxTingkatSekolah.setSelectedItem(Level.Level1.SMA);
                jComboBoxTingkatKelas.setSelectedItem(Level.Level2.SEPULUH);
                break;
            case "P":
                jComboBoxTingkatSekolah.setSelectedItem(Level.Level1.SMP);
                jComboBoxTingkatKelas.setSelectedItem(Level.Level2.TUJUH);
                break;
            case "K":
                jComboBoxTingkatSekolah.setSelectedItem(Level.Level1.SMK);
                jComboBoxTingkatKelas.setSelectedItem(Level.Level2.SEPULUH);
                break;
            default :
                jComboBoxTingkatSekolah.setSelectedItem(Level.Level1.SMA);
                jComboBoxTingkatKelas.setSelectedItem(Level.Level2.SEPULUH);
                break;
        }
    }
