    private boolean clerkLogin(){
        try{
            Clerk clerk = new Clerk();
            String user = jTextFieldUsername.getText();
            char[] pass = jPasswordFieldPassword.getPassword();
            String passS = "";
            for (int i = 0; i < pass.length; i++){
               passS= passS.concat(Character.toString(pass[i]));
            }
            
            clerk = Control.login(user,passS);
            if(clerk.jabatan.toLowerCase().equals("pendaftaran")){
                AppFramePendaftaran appFramePendaftaran = new AppFramePendaftaran(clerk);
                JOptionPane.showMessageDialog(appFramePendaftaran, "Login Success");
                appFramePendaftaran.setVisible(true);
            }else{
                AppFrame appFrame = new AppFrame(clerk);
                JOptionPane.showMessageDialog(appFrame, "Login Success");
                appFrame.setVisible(true);
            }
            this.dispose();
            return true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Login Failed".concat("\r\n").concat(e.toString()));
            return false;
        }
    }
    
    
